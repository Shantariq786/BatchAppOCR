// Helper to get a string in specified language
func LocalizedString(key: String, language: String) -> String {
    let languageLowerCase = language.lowercased()
    let supportedLangs = ["en", "nl"]
   // var languageToGet = supportedLangs.contains()
    var languageToGet = supportedLangs.contains(languageLowerCase) ? languageLowerCase : "en"
    if languageToGet == "en" {
        languageToGet = "Base" // english langugae is 'base' dev language
    }
    if let path = Bundle.main.path(forResource: languageToGet, ofType: "lproj"),
        let bundle = Bundle(path: path) {
        let string = bundle.localizedString(forKey: key, value: nil, table: nil)
        return string
    } else {
        return NSLocalizedString(key, comment: "")
    }
}

func showSuccessHud(status: String?){
    SVProgressHUD.setBorderWidth(1.0)
    SVProgressHUD.setBorderColor(.green)
    SVProgressHUD.showSuccess(withStatus: status)
    SVProgressHUD.dismiss(withDelay: 0.5)
}
